// 두 개의 변수 age와 year를 선언한 다음, 값을 저장하고 출력해보자.
package ch2_variable;

public class VarEx1 {

	public static void main(String[] args) {
		int age = 26;
		int year = 0;
		
		System.out.println(age);
		System.out.println(year);
		
		year = 2000 + 26;
		age = age + 1;
		
		System.out.println(year);
		System.out.println(age);
		
		
	}

}
